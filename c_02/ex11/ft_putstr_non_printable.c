#include <unistd.h>

void	put_char(char c)
{
	write(1, &c, 1);
}

int	char_is_priintable(char c)
{
	if (c >= 32 && c <= 126)
		return (1);
	else
		return (0);
}

void	ft_putstr_non_printable(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (char_is_printable(str[i] == 1))
			put_char(str[i]);
		else
		{
			put_char('\\');
			ft_putchar("0123456789abcdef"[str[i] / 16]);
			ft_putchar("0123456789abcdef"[str[i] % 16]);
		}
		i++;
	}
}
